    `LAST_UPD_DATE` timestamp   NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`MEMBER_NO`)
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8;


